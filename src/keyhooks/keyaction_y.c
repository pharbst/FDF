#include "fdf.h"

void	keyaction_y(t_a *a)
{
	t_map	*node;

	if (a->keys.z && !a->keys.x)
		a->y -= 0.1;
	else if (a->keys.x && !a->keys.z)
		a->y += 0.1;
}
